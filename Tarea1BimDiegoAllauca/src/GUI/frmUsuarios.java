    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        //String estado = this.cmbEstado.getSelectedItem().toString();
        String identificacion = this.txtIdent.getText();
        String nombres = this.txtNombres.getText();
        String tipo = this.cmbTipo.getSelectedItem().toString();
        String tipoInt = tipo.equals("Secretaria") ? "2" : "1";
        String dependencia = this.cmbDepen.getSelectedItem().toString();
        String pass = String.valueOf(this.txtPass.getPassword());
        String passEnc = EncripterManager.encryptKey(pass);
        //validacion campos vacios
        if (identificacion.isEmpty() || nombres.isEmpty() || tipo.isEmpty() || dependencia.isEmpty() || pass.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Campos Vacios.");
        } else {
            boolean permiteIngreso = true;
            if (totalAdmin >= 1 || totalSec >= 5) {
                permiteIngreso = false;
            }
            if (permiteIngreso) {
                Administrativo usuario = new Administrativo();
                usuario.setIntPersona((totalAdmin + totalSec) + 1);
                usuario.setStrIdentificacion(identificacion);
                usuario.setStrNombres(nombres);
                usuario.setChrTipo(tipoInt);
                usuario.setStrDependencia(dependencia);
                usuario.setPass(passEnc);
                try {
                    txtParsing1.writeDataAdministrativos(usuario);
                    txtParsing1.lstAdministrativosObj.add(usuario);
                    JOptionPane.showMessageDialog(this, "Ingreso Exitoso.");
                    this.initialize();
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "Error.");
                    System.err.format("IOException: %s%n", ex);
                }
            } else {
                JOptionPane.showMessageDialog(this, "El sistema admite como maximo 5 Scretarias y 1 Administrador");
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

