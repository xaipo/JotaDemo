    public Administrativo getAdminById(String ci) {
        for (Administrativo adm : txtParsing1.lstAdministrativosObj) {
            if (adm.getStrIdentificacion().equals(ci)) {
                return adm;
            }
        }
        return null;
    }

    public int getAdminByIdNum(String ci) {
        int i = 0;
        for (Administrativo adm : txtParsing1.lstAdministrativosObj) {
            if (adm.getStrIdentificacion().equals(ci)) {
                return i;
            }
            i++;
        }
        return i;
    }

